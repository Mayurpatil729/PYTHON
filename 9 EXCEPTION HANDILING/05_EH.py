# Try -except - finally -else


try:
    print("try block")
    print(10/0)
except:
    print("except block")
else:
    print("Else block")
finally:
    print("Finally block")
    

##############################################################

# try:
# Risky Code
# except:
# will be executed if exception inside try
# else:
# will be executed if there is no exception inside try
# finally:
# will be executed whether exception raised or not raised and handled or not 
# handled


