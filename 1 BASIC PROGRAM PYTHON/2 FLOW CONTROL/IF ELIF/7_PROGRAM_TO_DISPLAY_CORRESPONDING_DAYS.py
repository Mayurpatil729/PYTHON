day = int(input("Enter the day number :"))

if day == 1:
    print("\t\n sunday ")
elif day == 2:
    print("\n\t Monday")
elif day == 3:
    print("\n\t Tuesday")
elif day == 4:
    print("\n\t wednesday")
elif day == 5:
    print("\n\t Thursday")
elif day == 6:
    print("\n\t Friday")
elif day == 7:
    print("\n\t Saturday")
else:
    print("\n\t Enter the valid number")
