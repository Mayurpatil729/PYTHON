
"""        Area of rectangle  🐬            """
from math import pi
print("\n\t Program to find area of rectangle ")
l = int(input("Enter the length of rectangle :"))
b = int(input("Enter the  breadth of rectangle :"))
print("\n\t The area of rectangle : ", 2*(l+b))


"""        Area of circle   💸            """
r = eval(input('\n\tEnter the radius of circle :'))
print("\n\t The area of circle  : ", pi*r**2)


"""        Area of square    💸        """
side = eval(input("\n\t Enter the side of square :"))
print("\n\t The area of square is :", side**2)


